static inline double interpola_cd(const curva_arrasto_t *curva, double mach)
{
    size_t i;

    if (curva == NULL || curva->pontos == NULL || curva->n_pontos == 0) {
        return 0.0;
    }

    if (mach <= curva->pontos[0].mach) {
        return curva->pontos[0].cd;
    }

    if (mach >= curva->pontos[curva->n_pontos - 1].mach) {
        return curva->pontos[curva->n_pontos - 1].cd;
    }

    for (i = 0; i < curva->n_pontos - 1; i++) {
        const double m1 = curva->pontos[i].mach;
        const double m2 = curva->pontos[i + 1].mach;

        if (mach >= m1 && mach <= m2) {
            const double cd1 = curva->pontos[i].cd;
            const double cd2 = curva->pontos[i + 1].cd;
            const double f = (mach - m1) / (m2 - m1);

            return cd1 + f * (cd2 - cd1);
        }
    }

    return curva->pontos[curva->n_pontos - 1].cd;
}

